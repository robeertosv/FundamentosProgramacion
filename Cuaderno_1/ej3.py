"""
    Calcular y mostrar por pantalla el área de un cuadrado
"""

#Con una variable para almacenar el lado y otra para almacenar el área
lado = 2
area = lado ** 2

print(f"El área es {area}")

#Lo mismo pero sin la variable para almacenar el área
lado = 2
print(f"El área del cuadrado es {lado**2}")